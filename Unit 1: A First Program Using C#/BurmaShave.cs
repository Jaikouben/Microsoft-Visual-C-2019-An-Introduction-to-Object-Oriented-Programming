/*
From 1925 through 1963, Burma Shave advertising signs appeared next to highways all across the United States. 
There were always four or five signs in a row containing pieces of a rhyme, followed by a final sign that read “Burma Shave.” 
For example, one set of signs that has been preserved by the Smithsonian Institution reads as follows:

Shaving brushes
You’ll soon see ’em
On a shelf
In some museum
Burma Shave

Instructions
Find a classic Burma Shave rhyme on the Web and write a program named BurmaShave that displays the rhyme.
*/

using static System.Console;
class BurmaShave
{
   static void Main()
   {
    // Display Burma Shave rhymes
    WriteLine("Shaving brushes");
    WriteLine("You'll soon see 'em");
    WriteLine("On a shelf");
    WriteLine("In some museum");
    WriteLine("Burma Shave");
   }
}
